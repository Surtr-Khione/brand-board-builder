#!/usr/bin/env node
// Generate journal breakdowns for library brands → src/lib/blogBreakdowns.js
// Usage: SUPABASE_SERVICE_KEY=... BMD_INTERNAL_KEY=... node scripts/brand-articles.mjs slug1 slug2 ...
import { writeFileSync } from "node:fs";

const B = "https://bukgitgwwmzdjibekmzb.supabase.co";
const H = { "Content-Type": "application/json", Authorization: `Bearer ${process.env.SUPABASE_SERVICE_KEY}`, apikey: process.env.SUPABASE_SERVICE_KEY, "x-bmd-internal": process.env.BMD_INTERNAL_KEY || "" };

const slugs = process.argv.slice(2);
const posts = [];
for (const slug of slugs) {
  try {
    const br = await fetch(`${B}/functions/v1/search-brands?slug=${slug}`, { headers: H });
    const { brand } = await br.json();
    const ar = await fetch(`${B}/functions/v1/brand-article`, { method: "POST", headers: H, body: JSON.stringify({ brand }) });
    const d = await ar.json();
    if (d.error) throw new Error(d.error);
    posts.push({
      slug: `inside-${slug}`,
      title: d.article.title,
      dek: d.article.dek,
      tag: "Breakdown",
      date: new Date().toISOString().slice(0, 10),
      readMins: Math.max(3, Math.round(d.article.paragraphs.join(" ").split(/\s+/).length / 220)),
      brandSlug: slug,
      body: d.article.paragraphs,
    });
    console.log(`✓ ${slug}: ${d.article.title}`);
  } catch (e) { console.log(`✗ ${slug}: ${e.message}`); }
}
writeFileSync("src/lib/blogBreakdowns.js",
  `// Generated by scripts/brand-articles.mjs — journal breakdowns of library brands.\n// Regenerate after enrichment changes; each links back to its brand page.\nexport const BREAKDOWN_POSTS = ${JSON.stringify(posts, null, 2)};\n`);
console.log(`Wrote ${posts.length} breakdowns.`);
